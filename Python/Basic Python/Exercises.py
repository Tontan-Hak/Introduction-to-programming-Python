# The following code example would print the data type of x, what data type would that be?
x = ["apple", "banana", "cherry"]
print(type(x))
# ->list

# The following code example would print the data type of x, what data type would that be?
x = ("apple", "banana", "cherry")
print(type(x))
# -> tuple

# The following code example would print the data type of x, what data type would that be?
x = {"name" : "John", "age" : 36}
print(type(x))
# -> dict

# The following code example would print the data type of x, what data type would that be?
x = True
