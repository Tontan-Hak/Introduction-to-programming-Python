# Tuple
thistuple = ("apple", "banana", "cherry")
print(thistuple)

# Tuple Length
thistuple = tuple(("apple", "banana", "cherry"))
print(len(thistuple))

# Create Tuple With One Item
thistuple = ("apple",)
print(type(thistuple))

#NOT a tuple
thistuple = ("apple")
print(type(thistuple))

# Access Tuple
Fruits = ("apple", "banana","cherry")
print(Fruits[2])